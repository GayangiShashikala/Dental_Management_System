    private void DisplayAppoitments()
    {
        try {
            Class.forName("com.mysql.jdbc.Driver");
            conn=DriverManager.getConnection("jdbc:mysql://localhost:3306/dental_management","root","");
            st=(Statement) conn.createStatement();
            rs=st.executeQuery("Select * from appointment_details");
            
            
            while(rs.next())
            {
                String ID=String.valueOf(rs.getString("App_ID"));
                String patient_Name=String.valueOf(rs.getString("P_Name"));
                String Date=String.valueOf(rs.getString("Date"));
                String Time=String.valueOf(rs.getString("Time"));
                String Treatment=String.valueOf(rs.getString("Treatments"));
                
                String tbData[]={ID,patient_Name,Date,Time,Treatment};
                tblModel=(DefaultTableModel)tbl_appointment.getModel();
                
         
                tblModel.addRow(tbData);
            }
        } catch (Exception e) {
        }
    }
    
