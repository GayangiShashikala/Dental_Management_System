    private void btn_A_saveMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_A_saveMouseClicked
       if(cmb_patient.getSelectedIndex() == -1 || cmb_treatment.getSelectedIndex()== -1 )
       {
           
           JOptionPane.showMessageDialog(this, "Missing Information");
       }
       else
       {
        
        try{
           
           A_ID_Count();
           Class.forName("com.mysql.jdbc.Driver");
           conn=DriverManager.getConnection("jdbc:mysql://localhost:3306/dental_management","root","");
           String sql = "insert into Appointment_details values (?,?,?,?,?)";
           PreparedStatement add= conn.prepareStatement(sql);
           
           add.setInt(1, appoinment_ID);           
           add.setString(2, cmb_patient.getSelectedItem().toString());
           add.setString(3, Dc_Date.getDate().toString());
           add.setString(4, String.valueOf(Dc_time.getDate().getTime()));
           add.setString(5, cmb_treatment.getSelectedItem().toString());
           
           int row=add.executeUpdate();
           JOptionPane.showMessageDialog(this, "Appointment Insereted Successfully");
           conn.close();
           tblModel.setRowCount(0);
          DisplayAppoitments();
          Clear();
          
           
         }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
         }
       }       
    }//GEN-LAST:event_btn_A_saveMouseClicked

